public class relational_operator {
    public static void main(String[] args) {
         int a = 10;
        int b = 20; //pre increment
        System.out.println(a == b);
        System.out.println(a != b);
        System.out.println(a > b);
        System.out.println(a < b);
        System.out.println(a >= b);
        System.out.println(a <= b);
    }
           
}
